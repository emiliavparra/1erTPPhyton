# Crear un diccionario
estudiante = {
    'nombre': 'Laura',
    'edad': 22,
    'curso': 'Ingenieria',
    'ciudad': 'Barcelona'
}
# Imprimimos el diccionario
print('Diccionario original: ', estudiante)

# Eliminar el elemento con la clave 'curso'
# usando DEL

del estudiante['curso']  # Imprimo el resultado
print('Con curso eliminado: ', estudiante)

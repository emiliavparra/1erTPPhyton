# Ejemplos

# Asignacion simple
a = 10
print(f'a = {a}')

# Suma por asignacion
a += 5
print(f'a += 5: {a}')

# Resta por asignacion
a -= 3
print(f'a -= 3: {a}')

# Multiplicacion y asignacion
a *= 4
print(f'Multiplicacion a*= 4: {a}')

# Division y asignacion
a /= 2
print(f'Division a/= 2: {a}')

# Division entera y asignacion
a //= 3
print(f'Division entera a//=3 : {a}')

# Modulo y asignacion
a %= 4
print(f'Modulo  a%= 4 : {a}')

# Exponente y asignacion
a **= 2
print(f'Exponente a**= 2: {a}')

# Ejemplo de operadores relacionales

a = 10
b = 5

# Igual a
resultado_igual = (a == b)
print(f'{a} es igual a {b}? {resultado_igual}')

# No igual a
resultado_no_igual = (a != b)
print(f'{a} es diferente de {b}? {resultado_no_igual}')

# Mayor que
resultado_mayor = (a > b)
print(f'{a} es mayor que {b}? {resultado_mayor}')

# Menor que
resultado_menor = (a < b)
print(f'{a} es menor que {b}? {resultado_menor}')

# Mayor o igual que
resultado_mayor_igual = (a >= b)
print(f'{a} es mayor o igual que {b}? {resultado_mayor_igual}')

# Menor o igual que
resultado_menor_igual = (a <= b)
print(f'{a} es menor o igual que {b}? {resultado_menor_igual}')
